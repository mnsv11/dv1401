<p>Kransar gjorda av glaspärlor hängdes på gravkorset eller lades på graven. 
Den tunna tråden med glaspärlor formades till spiraler, blad eller blommor. Bruket 
kom ursprungligen från Italien och Frankrike men började förekomma i Sverige under 
senare delen av 1800-talet. Man lade den på kistan i hemmet och sedan följde kransen
kistan till gravsättningen då man lade kransen på graven.
<br><br>
Trots att metalltråden rostade och glaspärlorna kunde gå sönder av frosten varade 
kransarna längre än de av naturligt material. Efter en tid tog man hem kransen och 
förvarade den på loge eller vind för att användas vid nästa dödsfall. Först på 30-talet 
försvann de från kyrkogårdarna; på landsbygden kunde de dock ses ända in på 50- och 60-talen.

</p>

