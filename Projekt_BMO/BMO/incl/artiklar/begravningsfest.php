<p>När ett dödsfall inträffat, fick de närmaste anhöriga bråda dagar. Den döde 
skulle med heder och aktning komma i jorden. Men det gällde också att rusta för 
begravningskalaset. Ofta skedde bjudningen på själva begravningsdagen. Inbjudning 
skickades ut i samband med att dödsfallet meddelades. De som var bjudna dök oftast 
upp, man tackade hellre nej till bröllop än till en begravning.
<br><br>
Vid större begravningskalas hade gästerna med sig "förning", husmödrarna rådslog 
vilken förning som skulle medtagas så att det blev lagom mycket av varje sort. 
vanliga matvaror i en förningskorg var bröd, mjölk, smör, ost, ägg, gröt, färdiga 
stekar, fårlår, höns, fläsk, äggpannkakor, puddingar, tårtor och kakor.
<br><br>
Ett begravningskalas var lika ståtligt och storartat som ett bröllop. Man dukade 
i vitt och svart i husets finaste rum, detta var ofta samma rum som den döde stått 
lik i, på detta sätt ansåg man att man hedrade den avlidne. Var huset för litet så 
dukade man även hos grannen. Ett kalas kunde pågå i två gårdar samtidigt.
<br><br>
Borden prålade av maträtter och dryck. Man åt i omgångar, smårgåsbord, soppa, 
varmrätt och efterätt.
<br><br>
Vid en begravning anno 1795 skrev Märta Helena Reenstierna följande om maten:
<br><br>
"Efter jordfästningen kommo alla hit, och undfägnades först med Renskt vin och 
Confect, sedan med en Soupée af 10 rätter, Désere, Sylt, Pounch, Biscop (bål), 
och viner."
<br><br>
Känsloläget var ofta långt ifrån bedrövat vid dessa tillställningar. Man kunde 
höra gästerna säga "Det var en riktigt trevlig begravning!". Det var inte alla 
bröllop som nådde samma nivå av glädje och uppslupenhet. Man skämtade, berättade 
historier, gissade gåtor och spelade kort. Ibland dansade man till och med." 
<br><br>
Källa: "Seder och bruk vid livets slut, bok av Lars Bondeson", 


